    let c5 = document.getElementById("c5");
    config = new Config();
    config.add("background", c1);
    config.add("map", c2);
    config.add("zoomers", c3);
    config.add("FPS", c4);
    config.add("grid", c5);
    //
    // Zoomers
    //
    // [TODO] spawn from config 
    //
    let zoomer, zoomer1, zoomer2, zoomer3, zoomer4, zoomer5;
