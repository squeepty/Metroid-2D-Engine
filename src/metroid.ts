    zoomer1.spawn(map.data, zoomer1.COUNTERCLOCK, zoomer1.DOWN, 9, 12);
    zoomer2 = new Zoomer();
    zoomer2.spawn(map.data, zoomer2.CLOCK, zoomer2.TOP, 19, 4);
    zoomer3 = new Zoomer();
    zoomer3.spawn(map.data, zoomer3.COUNTERCLOCK, zoomer3.DOWN, 19, 17);
    zoomer4 = new Zoomer();
